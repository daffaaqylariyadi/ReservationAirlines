        private void handleLogin() {
        String username_user = txt_username.getText().trim();
        String password_user = new String(txt_password.getPassword());
        
        // Validasi input kosong
        if (username_user.isEmpty() || password_user.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua data harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (dao_abstract.loginPassenger(username_user, password_user)) {
        JOptionPane.showMessageDialog(this, "Login berhasil!", "Success", JOptionPane.INFORMATION_MESSAGE);
        
        this.setVisible(false); 
        FormMainAdmin mainFrame = new FormMainAdmin();
        mainFrame.setVisible(true);
        } else {
        JOptionPane.showMessageDialog(this, "Username atau password salah.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
            
